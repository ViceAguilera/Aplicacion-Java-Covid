    public void mostrarDatos(){
        while (modelo.getRowCount()>0) {            
            modelo.removeRow(0);
        }

        conexion objConexion4= new conexion();

        try {
            ResultSet resultado= objConexion4.consultarRegistros("SELECT * FROM ResidenciaSanitaria");
            while (resultado.next()) {

                Object[] oUsuario= {resultado.getString("IDResidencia"),resultado.getString("Nombre"),resultado.getString("Direccion"),resultado.getString("Region"),resultado.getString("ID_centromedico")};
                
                modelo.addRow(oUsuario);
                }
        } catch (Exception e) {
            System.out.println(e);
        }       

    }    

    public void mostrarDatos2(){
        while (modelo2.getRowCount()>0) {            
            modelo2.removeRow(0);
        }

        conexion objConexion2= new conexion();

        try {
            ResultSet resultado= objConexion2.consultarRegistros("SELECT IDCentroMedico,Tipo,Region FROM CentrosMedicos");
            while (resultado.next()) {

                Object[] oUsuario= {resultado.getString("IDCentroMedico"),resultado.getString("Tipo"),resultado.getString("Region")};
                
                modelo2.addRow(oUsuario);
                }
        } catch (Exception e) {
            System.out.println(e);
        }   
    }

    public ResidenciaBL recuperarDatosGUI(){
        ResidenciaBL residenciaSanitaria= new ResidenciaBL();
  
        int IDresidencia= (id_residenciaSanitaria.getText().isEmpty())?0: Integer.parseInt(id_residenciaSanitaria.getText());
        int idCentro= (idCentro_residencia.getText().isEmpty())?0: Integer.parseInt(idCentro_residencia.getText());
        
        residenciaSanitaria.setIDResidencia(IDresidencia);
        residenciaSanitaria.setNombre(nombre_residenciaSanitaria.getText());
        residenciaSanitaria.setDireccion(direccion_residenciaSanitaria.getText());
        residenciaSanitaria.setRegion(region_Residencia.getText());
        residenciaSanitaria.setID_centromedico(idCentro);

        return residenciaSanitaria;
    }

