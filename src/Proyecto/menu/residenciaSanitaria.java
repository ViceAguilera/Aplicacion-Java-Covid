    private void btnAgregar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregar1ActionPerformed
        // TODO add your handling code here:
        if (nombre_residenciaSanitaria.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "No ingresaste el nombre de la residencia sanitaria", "Error", JOptionPane.WARNING_MESSAGE);
            nombre_residenciaSanitaria.requestFocus();
        }
        else if (direccion_residenciaSanitaria.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "No ingresaste la direccion de la residencia sanitaria", "Error", JOptionPane.WARNING_MESSAGE);
            direccion_residenciaSanitaria.requestFocus();
        }
        else if (idCentro_residencia.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "No ingresaste el id de la residencia sanitaria", "Error", JOptionPane.WARNING_MESSAGE);
            idCentro_residencia.requestFocus();
        } 
        else{
            conexion objConexion4= new conexion();

            ResidenciaBL residenciaSanitaria= recuperarDatosGUI();

            String strSentenciaInsert= String.format("INSERT INTO ResidenciaSanitaria (IDResidencia,Nombre,Direccion,Region,ID_centromedico) " 
                        + "VALUES (NULL,'%s','%s','%s','%s')",residenciaSanitaria.getNombre(),residenciaSanitaria.getDireccion(),residenciaSanitaria.getRegion(),residenciaSanitaria.getID_centromedico());

            objConexion4.ejecutarSentenciaSQL(strSentenciaInsert);
        
            this.mostrarDatos();
            this.limpiar();
        }
    }//GEN-LAST:event_btnAgregar1ActionPerformed

