    private void btnAgregar2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregar2ActionPerformed
        // TODO add your handling code here:
        if (nombre_medico.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "No ingresaste el nombre del medico", "Error", JOptionPane.WARNING_MESSAGE);
            nombre_medico.requestFocus();
        }
        else if (apellido_medico.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "No ingresaste el apellido del medico", "Error", JOptionPane.WARNING_MESSAGE);
            apellido_medico.requestFocus();
        }
        else if (genero_medico.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "No ingresaste el genero del medico", "Error", JOptionPane.WARNING_MESSAGE);
            genero_medico.requestFocus();
        }
        else if (edad_medico.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "No ingresaste la edad del medico", "Error", JOptionPane.WARNING_MESSAGE);
            genero_medico.requestFocus();
        } 
        else if (edad_medico.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "No ingresaste la edad del medico", "Error", JOptionPane.WARNING_MESSAGE);
            genero_medico.requestFocus();
        } 
        else if (18 > Integer.parseInt(edad_medico.getText())){
            JOptionPane.showMessageDialog(this, "No ingresaste la edad del medico es menor a 18", "Error", JOptionPane.WARNING_MESSAGE);
            genero_medico.requestFocus();
        } 
         else if (Integer.parseInt(edad_medico.getText())>65){
            JOptionPane.showMessageDialog(this, "No ingresaste la edad del medico es mayor a 65", "Error", JOptionPane.WARNING_MESSAGE);
            genero_medico.requestFocus();
        } 
        else if (idCentro_medico.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "No ingresaste la id del centro medico del medico", "Error", JOptionPane.WARNING_MESSAGE);
            idCentro_medico.requestFocus();
        } 
        else{
            conexion objConexion3= new conexion();

            MedicoBL medicoO= recuperarDatosGUI();

            String strSentenciaInsert= String.format("INSERT INTO Medicos (IDMedico,Nombre,Apellido,Especialidad,Genero,Edad,ID_centromedico) " 
                        + "VALUES (NULL,'%s','%s','%s','%s','%s','%s')",medicoO.getNombre(),medicoO.getApellido(),medicoO.getEspecialidad(),medicoO.getGenero(),medicoO.getEdad(),medicoO.getID_centromedico());

            objConexion3.ejecutarSentenciaSQL(strSentenciaInsert);
        
            this.mostrarDatos();
            this.limpiar();
        }   
    }//GEN-LAST:event_btnAgregar2ActionPerformed

