    public void mostrarDatos(){
        while (modelo.getRowCount()>0) {            
            modelo.removeRow(0);
        }

        conexion objConexion3= new conexion();

        try {
            ResultSet resultado= objConexion3.consultarRegistros("SELECT * FROM Medicos");
            while (resultado.next()) {

                Object[] oUsuario= {resultado.getString("IDMedico"),resultado.getString("Nombre"),resultado.getString("Apellido"),resultado.getString("Especialidad"),resultado.getString("Genero"),resultado.getString("Edad"),resultado.getString("ID_centromedico")};
                
                modelo.addRow(oUsuario);
                }
        } catch (Exception e) {
            System.out.println(e);
        }       

    }

